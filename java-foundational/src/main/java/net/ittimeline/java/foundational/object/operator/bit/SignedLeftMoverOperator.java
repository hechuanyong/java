/**
 * 有符号左移动
 *
 * @author tony 254280671@qq.com
 * @version 2024/3/31 10:05
 * @since JDK11
 */
public class SignedLeftMoverOperator {
    public static void main(String[] args) {
        //10        0000 0000 0000 0000 0000 0000 0000 1010  <<2
        //          00 0000 0000 0000 0000 0000 0000 10100
        //          00 0000 0000 0000 0000 0000 0000 10100
        System.out.println(10 << 2);
        //          -10    元码    1000 0000 0000 0000 0000 0000 0000 1010
        //                  反码   1111 1111 11111 1111 1111 1111 1111 0101
        //                  补码   1111 1111 11111 1111 1111 1111 1111 0110
        //          <<2

        //                     11 1111 11111 1111 1111 1111 1111 011000
        ///        反码-1：     11 1111 11111 1111 1111 1111 1111 010111
        //          元码          10 0000 0000 0000 0000 0000 0000 101000
        //         -40
        System.out.println(-10 << 2);

        //正整数有符号右移 >>
        /* 32     0000 0000 0000 0000 0000 0000 0010 0000 >>2
                  000000 0000 0000 0000 0000 0000 0010 00


         */
        System.out.println(32 >> 2);

        //负整数有符号右移 >>
        /*
            /* -32     元码        1000 0000 0000 0000 0000 0000 0010 0000
        //                  反码   1111 1111 11111 1111 1111 1111 1101 1111
        //                  补码   1111 1111 11111 1111 1111 1111 1110 0000 >>2
                                //111111 1111 11111 1111 1111 1111 1110 00
                     //     反码   111111 1111 11111 1111 1111 1111 1101 11
        //                  元码   100000 0000 0000 0000 0000  0010 00   -8

         */
        System.out.println(-32 >> 2);
    }
}
