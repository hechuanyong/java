public class Varible {
    public static void main(String[] args) {
        // 数据类型 变量名 = 变量值；
        int age = 29;
        System.out.println(age);
        age = 30;
        System.out.println(age);
        int value;
        value = 10;
        System.out.println(value);
    }
}
